def callout(title, text):
    table = DOC.add_table(rows=1, cols=1)
    set_table_geometry(table, [6.5])
    cell = table.cell(0, 0)
    shade_cell(cell, FILL_CALLOUT)
    p = cell.paragraphs[0]
    p.paragraph_format.space_after = Pt(2)
    p.paragraph_format.line_spacing = 1.1
    r = p.add_run(title)
    set_run_font(r, size=11, color=INK, bold=True)
    p.add_run("\n")
    r = p.add_run(text)
    set_run_font(r, size=11, color=INK)
    paragraph(after=4)
    return table


def table(headers, rows, widths, header_fill=FILL_HEADER):
    t = DOC.add_table(rows=1, cols=len(headers))
    set_table_geometry(t, widths)
    set_repeat_header(t.rows[0])

    for idx, header in enumerate(headers):
        cell = t.cell(0, idx)
        shade_cell(cell, header_fill)
        p = cell.paragraphs[0]
        p.paragraph_format.space_after = Pt(0)
        r = p.add_run(header)
        set_run_font(r, size=10.5, color=INK, bold=True)

    for row in rows:
        cells = t.add_row().cells
        for idx, value in enumerate(row):
            cell = cells[idx]
            p = cell.paragraphs[0]
            p.paragraph_format.space_after = Pt(0)
            p.paragraph_format.line_spacing = 1.05
            r = p.add_run(value)
            set_run_font(r, size=10)

    set_table_geometry(t, widths)
    paragraph(after=4)
    return t


def image_block(path, caption, width):
    if not path.exists():
        return
    p = DOC.add_paragraph()
    p.alignment = WD_ALIGN_PARAGRAPH.CENTER
    p.paragraph_format.space_after = Pt(2)
    run = p.add_run()
    pic = run.add_picture(str(path), width=Inches(width))
    pic._inline.docPr.set("descr", caption)
    c = DOC.add_paragraph()
    c.alignment = WD_ALIGN_PARAGRAPH.CENTER
    c.paragraph_format.space_after = Pt(8)
    r = c.add_run(caption)
    set_run_font(r, size=9.5, color=MUTED, italic=True)


